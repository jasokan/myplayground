# Some examples on Data types

intvalue = 10
print(type(intvalue))

floatvalue = 11.9
print(type(floatvalue))

strvalue = 'string'
print(type(strvalue))

fint=5
sint=10
complexvalue=complex(fint,sint)
print(type(complexvalue))

tuplevalue = ("School", "students", "exam")
print(type(tuplevalue))

rangevalue = range(10)
print(type(rangevalue))

setvalue = {7,8}
print(type(setvalue))

listvalue = [7,70,'test','90']
print(type(listvalue))

dictvalue = {"name":"Vicky", "place":"bangalore"}
print(type(dictvalue))
